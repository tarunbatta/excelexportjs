/**
 * Represents the horizontal alignment of cell content.
 * Controls how text is positioned within a cell horizontally.
 */
export enum HorizontalAlignment {
    /** Center the content horizontally */
    Center = 'Center',
    /** Align content to the left */
    Left = 'Left',
    /** Align content to the right */
    Right = 'Right',
}

/**
 * Represents the vertical alignment of cell content.
 * Controls how text is positioned within a cell vertically.
 */
export enum VerticalAlignment {
    /** Align content to the bottom */
    Bottom = 'Bottom',
    /** Center the content vertically */
    Center = 'Center',
    /** Align content to the top */
    Top = 'Top',
}
